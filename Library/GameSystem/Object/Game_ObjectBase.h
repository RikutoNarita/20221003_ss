// インクルードガード
#ifndef __OBJECT_BASE_H__
#define __OBJECT_BASE_H__

// インクルード
#include <GraphicsSystem\Interface\Gfx_GraphiccsBinder.h>
#include <memory>

// クラス定義
class GameObjectBase
{
public:
    //------------------------------------------------------------------------------

    //------------------------------------------------------------------------------
    /// コンストラクタ
    ///
    /// \return void
    //------------------------------------------------------------------------------
    GameObjectBase();

    //------------------------------------------------------------------------------
    /// デストラクタ
    ///
    /// \return void
    //------------------------------------------------------------------------------
    ~GameObjectBase();

    //------------------------------------------------------------------------------
    /// オブジェクトの初期化
    ///
    /// \return 初期化処理の成否
    //------------------------------------------------------------------------------
    virtual void Init();

    //------------------------------------------------------------------------------
    /// オブジェクトの終了処理
    ///
    /// \return void
    //------------------------------------------------------------------------------
    virtual void Uninit();

    //------------------------------------------------------------------------------
    /// オブジェクトの更新
    ///
    /// \return void
    //------------------------------------------------------------------------------
    virtual void Update();

    //------------------------------------------------------------------------------
    /// オブジェクトの描画
    ///
    /// \return void
    //------------------------------------------------------------------------------
    virtual void Draw();

    //------------------------------------------------------------------------------
    /// 座標の設定
    ///
    /// \return void
    //------------------------------------------------------------------------------
    void SetPos(float x, float y, float z = 0.0f);

    //------------------------------------------------------------------------------
    /// テクスチャの設定
    ///
    /// \return void
    //------------------------------------------------------------------------------
    void SetTexture(GfxTexture* pTex);

    //------------------------------------------------------------------------------
    /// ピクセルシェーダーの設定
    ///
    /// \return void
    //------------------------------------------------------------------------------
    void SetPS(GfxPixelShader* pPS);

    //------------------------------------------------------------------------------
    /// 頂点シェーダーの設定
    ///
    /// \return void
    //------------------------------------------------------------------------------
    void SetVS(GfxVertexShader* pVS);

    //------------------------------------------------------------------------------

protected:
    //------------------------------------------------------------------------------
    std::shared_ptr<GfxGraphicsBinder> m_pGraphics;
    float m_fPos[3];
    //------------------------------------------------------------------------------
    /// <summary>
    /// m_pGraphics 描画機能
    /// m_fPos      座標
    /// </summary> 

};
#endif // __OBJECT_BASE_H__
