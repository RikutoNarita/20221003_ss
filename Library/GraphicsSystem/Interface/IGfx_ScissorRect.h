// インクルードガード
#ifndef __SCISSOR_RECT_H__
#define __SCISSOR_RECT_H__

// クラス定義
class IGfxScissorRect
{
public:
    //------------------------------------------------------------------------------
    struct Description
    {
        unsigned int top;
        unsigned int left;
        unsigned int right;
        unsigned int bottom;
    };

    //------------------------------------------------------------------------------
    /// <summary>
    /// top         // 切り抜き上座標
    /// left        // 切り抜き左座標
    /// right       // 切り抜き右座標
    /// bottom      // 切り抜き下座標
    /// </summary>

public:
    //------------------------------------------------------------------------------

    //------------------------------------------------------------------------------
    /// コンストラクタ
    ///
    /// \return void
    //------------------------------------------------------------------------------
    IGfxScissorRect() {};

    //------------------------------------------------------------------------------
    /// デストラクタ
    ///
    /// \return void
    //------------------------------------------------------------------------------
    virtual ~IGfxScissorRect() {};

    //------------------------------------------------------------------------------
    /// シザー矩形の指定
    ///
    /// \return void
    //------------------------------------------------------------------------------
    virtual void Bind() const = 0;

    //------------------------------------------------------------------------------
};
#endif // __SCISSOR_RECT_H__
